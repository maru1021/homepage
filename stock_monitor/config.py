def get_all_market_tickers():
    """MARKET_OVERVIEW の全ティッカーを {ticker: name} dict で返す"""
    result = {}
    for category in MARKET_OVERVIEW.values():
        for ticker, name in category:
            result[ticker] = name
    return result
